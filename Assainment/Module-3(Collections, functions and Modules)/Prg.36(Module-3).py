# How Do You Check The Presence Of A Key In A Dictionary?

#define dictionary and initialise key and values
Check_Dict={"Car":1,"Bike":2,"Bicycle":3,"Truck":4,"Bus":5}

#check any key in dictionary,if key include dictionary so True otherwise False
